class RelationOperator {
      public static void main(String[] args){
        int a = 5;
        int b = 5;

        System.out.println("a and b equal: " + (a==b) ); //output: true
        System.out.println("a and b less then: " + (a<b) ); //output: false
        System.out.println("a and b greater then: " + (a>b) ); //output: false
        System.out.println("a and b not equal: " + (a!=b) ); //output: false
        System.out.println("a and b less then equal: " + (a<=b) ); //output: true
        System.out.println("a and b greater then equal: " + (a>=b )); //output: true

           
      }
}
